# ═══════════════════════════════════════════════════════════════════
# Definition
# ═══════════════════════════════════════════════════════════════════

class ScriptParsePrompt(PromptDefinition):
    def __init__(self):
        super().__init__(
            key="script_parse",
            category="script",
            description="剧本结构化解析 — 原文 → JSON (scenes + dialogues)",
        )
        self.slots = [
            slot("role", ROLE, editable=True),
            slot("fidelity", FIDELITY, editable=True),
            slot("parsing_rules", PARSING_RULES, editable=True),
            slot("format", FORMAT, editable=False),
            slot("rules", RULES, editable=True),
            slot("language", LANGUAGE, editable=False),
        ]
    
    def build(self, overrides=None, params=None):
        sc = overrides or {}
        r = lambda k: resolve(sc, self.slots, k)
        return "\n\n".join([
            r("role"),
            r("fidelity"),
            r("parsing_rules"),
            r("format"),
            r("rules"),
            r("language"),
        ])
    
    def build_user_prompt(self, source_text: str) -> str:
        """构建 user prompt — 传入原始文本"""
        return f"""请将以下文本解析为结构化剧本 JSON：

--- 原始文本 ---
{source_text}
--- 结束 ---

重要：输出语言必须与原文相同。"""


def build_script_parse(source_text: str, overrides: Optional[Dict[str, str]] = None) -> dict:
    """一站式：返回 {'system': str, 'user': str}"""
    p = ScriptParsePrompt()
    return {
        "system": p.build(overrides),
        "user": p.build_user_prompt(source_text),
    }